% --- Executes on button press in pushbutton1.
function pushbutton1_Callback(hObject, eventdata, handles)
% hObject    handle to pushbutton1 (see GCBO)
% eventdata  reserved - to be defined in a future version of MATLAB
% handles    structure with handles and user data (see GUIDATA)
syms x;
y = get(handles.edit1, 'String');
b = get(handles.edit2, 'String');
xo = str2double(get(handles.edit3, 'String'));
error1 = str2double(get(handles.edit4,'String'));

    fun = inline(y);
    fund = inline(b);
    e=error1;
    fa=feval(fun,xo);
    fb=feval(fund,xo);
    x1=xo-(fa/fb);
    fa1=feval(fun,x1);
    fb1=feval(fund,x1);
    x2=x1-(fa1/fb1);
    error=abs(x2-x1);
    aux=x2;
    k=0;
    
    while error > e
        fa2=feval(fun,aux);
        fb2=feval(fund,aux);
        x3=aux-(fa2/fb2);
        error=abs(x3-aux);
        aux=x3;
        k=k+1;
        
    end
    
    
    res=k;
    
set(handles.edit6,'String',error);
set(handles.edit5, 'String', x3); 


axes(handles.axes1);
x1 = [-10:0.01:10];
x2 = subs(y,x,x1);
plot(x1,x2,'red','LineWidth', 2.5);
hold on
x2 = subs(b,x,x1);
plot(x1,x2,'blue','LineWidth', 2.5);


